int	ft_strict_pos_atoi(const char *s)
{
	int	res;
	int	i;

	res = 0;
	i = 0;
	while (s[i] && (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13)))
		i++;
	if (s[i] == '+')
		i++;
	if (!s[i])
		return (-1);
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + s[i] - '0';
		i++;
	}
	if (s[i] > 0 && (s[i] < '0' || s[i] > '9'))
		return (-1);
	return (res);
}
